/// LockApi2 stores table locks in a separate keyspace and creates revisions with PutSequential.
#[async_trait::async_trait]
pub trait LockApi2
where
    Self: Send + Sync,
    Self: kvapi::KVApi<Error = MetaError>,
{
    #[fastrace::trace]
    async fn list_lock_revisions_v2(
        &self,
        req: ListLockRevReq,
    ) -> Result<Vec<(u64, LockMeta)>, KVAppError> {
        let dir = req.lock_key.gen_v2_prefix();
        let mut strm = self.list_pb(ListOptions::unlimited(&dir)).await?;

        let mut list = Vec::new();
        while let Some(itm) = strm.try_next().await? {
            let revision = itm
                .key
                .try_revision()
                .map_err(|e| invalid_reply(format!("invalid table lock v2 revision: {}", e)))?;
            list.push((revision, itm.seqv.data));
        }

        Ok(list)
    }

    #[logcall::logcall]
    #[fastrace::trace]
    async fn create_lock_revision_v2(
        &self,
        req: CreateLockRevReq,
    ) -> Result<CreateLockRevReply, KVAppError> {
        debug!(req :? =(&req); "LockApi2: {}", func_name!());

        let lock_key = &req.lock_key;
        let lock_meta = LockMeta {
            user: req.user,
            node: req.node,
            query_id: req.query_id,
            created_on: Utc::now(),
            acquired_on: None,
            lock_type: lock_key.lock_type(),
            extra_info: lock_key.get_extra_info(),
        };

        let buf = encode_pb(&lock_meta);
        let op = TxnOp::put_sequential(
            lock_key.gen_v2_prefix().dir_name_with_slash(),
            TABLE_LOCK_SEQ_KEY_V2,
            buf,
        )
        .with_ttl(Some(req.ttl));

        let txn_req = TxnRequest::new(vec![], vec![op]);
        let (success, responses) = send_txn(self, txn_req).await?;
        if !success {
            return Err(invalid_reply(
                "PutSequential transaction unexpectedly failed",
            ));
        }

        let Some(put) = responses.first().and_then(|resp| resp.try_as_put()) else {
            return Err(invalid_reply("PutSequential did not return a put response"));
        };

        let key = TableLockIdentV2::from_str_key(&put.key).map_err(|e| {
            invalid_reply(format!("invalid table lock v2 key '{}': {}", put.key, e))
        })?;

        Ok(CreateLockRevReply {
            revision: key
                .try_revision()
                .map_err(|e| invalid_reply(format!("invalid table lock v2 revision: {}", e)))?,
        })
    }

    #[logcall::logcall]
    #[fastrace::trace]
    async fn extend_lock_revision_v2(&self, req: ExtendLockRevReq) -> Result<(), KVAppError> {
        debug!(req :? =(&req); "LockApi2: {}", func_name!());

        let ctx = func_name!();

        let lock_key = &req.lock_key;
        let table_id = lock_key.get_table_id();
        let key = lock_key.gen_v2_key(req.revision);

        self.crud_update_existing(
            &key,
            |mut lock_meta| {
                if req.acquire_lock {
                    lock_meta.acquired_on = Some(Utc::now());
                }
                Some((lock_meta, Some(req.ttl)))
            },
            || {
                Err(AppError::TableLockExpired(TableLockExpired::new(
                    table_id, ctx,
                )))
            },
        )
        .await??;
        Ok(())
    }

    #[logcall::logcall]
    #[fastrace::trace]
    async fn delete_lock_revision_v2(&self, req: DeleteLockRevReq) -> Result<(), KVAppError> {
        debug!(req :? =(&req); "LockApi2: {}", func_name!());

        let key = req.lock_key.gen_v2_key(req.revision);
        self.crud_remove(&key, || Ok::<(), ()>(())).await?.unwrap();

        Ok(())
    }

    #[logcall::logcall]
    #[fastrace::trace]
    async fn list_locks_v2(&self, req: ListLocksReq) -> Result<Vec<LockInfo>, KVAppError> {
        let mut reply = vec![];
        for dir in &req.prefixes {
            let table_id = dir.key().table_id();
            let lock = TableLockIdentV2::new(dir.key().tenant().clone(), table_id, 0);
            let dir = if table_id == 0 {
                DirName::new_with_level(lock, 2)
            } else {
                DirName::new(lock)
            };

            let mut strm = self.list_pb(ListOptions::unlimited(&dir)).await?;
            while let Some(itm) = strm.try_next().await? {
                let revision = itm
                    .key
                    .try_revision()
                    .map_err(|e| invalid_reply(format!("invalid table lock v2 revision: {}", e)))?;
                reply.push(LockInfo {
                    table_id: itm.key.table_id(),
                    revision,
                    meta: itm.seqv.data,
                });
            }
        }
        Ok(reply)
    }
}

fn invalid_reply(msg: impl ToString) -> KVAppError {
    let msg = msg.to_string();
    let source = io::Error::new(io::ErrorKind::InvalidData, msg.clone());
    InvalidReply::new(msg, &source).into()
}

#[async_trait::async_trait]
impl<KV> LockApi2 for KV
where
    KV: Send + Sync,
    KV: kvapi::KVApi<Error = MetaError> + ?Sized,
{
}
