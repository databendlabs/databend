//! Table lock v2 uses a separate meta-service keyspace and PutSequential.
//!
//! Key-value layout:
//! - `__fd_table_lock_v2/<tenant>/<table_id>/<revision>` -> `LockMeta`
//! - `revision` is the 21-digit, underscore-grouped suffix generated by
//!   `PutSequential`, e.g. `000_000_000_000_000_000_001`.
//! - `__fd_table_lock_v2_seq` stores the global sequence counter used by
//!   `PutSequential`; it is outside the lock-row prefix so listing locks only
//!   returns lock rows.
//! - Each lock row is written with the lock TTL and is periodically extended by
//!   the lock holder.
//!
//! Acquire algorithm:
//! 1. Insert a lock row with `PutSequential`; the generated suffix is the lock
//!    revision.
//! 2. List all rows under the same `<tenant>/<table_id>` prefix and sort by the
//!    numeric revision.
//! 3. The smallest revision owns the table lock and marks the row as acquired by
//!    extending it with `acquired_on`.
//! 4. Other waiters watch the delete event of their immediate predecessor, then
//!    repeat the list-and-check loop.
//! 5. Releasing the lock deletes the row; if a holder dies, TTL expiration
//!    eventually removes the row and lets the next waiter continue.
//!
//! The v2 keyspace is intentionally independent from the v1 table-lock keyspace.
//! During a rolling upgrade, old and new servers may acquire their own queue
//! entries concurrently; table metadata commit OCC still rejects conflicting
//! commits.

