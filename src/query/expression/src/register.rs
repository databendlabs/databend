            let generics = &(ctx.generics.to_owned());
            let arg2_iter = I2::iter_column(&arg2);
            let iter = arg2_iter.map(|arg2| func(arg1.clone(), arg2, ctx));
            let col = O::column_from_iter(iter, generics);
