    i_item_sk INTEGER,
    i_item_id VARCHAR,
    i_rec_start_date DATE,
    i_rec_end_date DATE,
    i_item_desc VARCHAR,
    i_current_price DECIMAL(7,2),
    i_wholesale_cost DECIMAL(7,2),
    i_brand_id INTEGER,
    i_brand VARCHAR,
    i_class_id INTEGER,
    i_class VARCHAR,
    i_category_id INTEGER,
    i_category VARCHAR,
    i_manufact_id INTEGER,
    i_manufact VARCHAR,
    i_size VARCHAR,
    i_formulation VARCHAR,
    i_color VARCHAR,
    i_units VARCHAR,
    i_container VARCHAR,
    i_manager_id INTEGER,
    i_product_name VARCHAR
);
